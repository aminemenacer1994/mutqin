function extractPlaceholders(s) {
  return (s.match(/\{[^}]+\}/g) || []).sort().join('|');
}

const en = JSON.parse(fs.readFileSync(EN_FLAT, 'utf8'));
const missing = [];
const bad = [];

for (const [path, enVal] of Object.entries(en)) {
  if (!(path in T)) missing.push(path);
  else {
    const idVal = T[path];
    if (typeof enVal === 'string' && typeof idVal === 'string') {
      if (extractPlaceholders(enVal) !== extractPlaceholders(idVal)) {
        bad.push(path);
      }
    }
  }
}

if (missing.length) {
  console.error('Missing translations:', missing.length);
  missing.slice(0, 20).forEach((k) => console.error(' ', k));
  process.exit(1);
}
if (bad.length) {
  console.error('Placeholder mismatch:', bad.length);
  bad.slice(0, 10).forEach((k) => console.error(' ', k, en[k], '->', T[k]));
  process.exit(1);
}

fs.writeFileSync(OUT, JSON.stringify(T, null, 2) + '\n');
console.log('Wrote', Object.keys(T).length, 'translations to', OUT);
